class Vector3:
    """Ultimate 3D vector mathematics for infinite precision"""
    
    def __init__(self, x: float = 0.0, y: float = 0.0, z: float = 0.0):
        """Initialize vector with infinite precision"""
        self.x = float(x)
        self.y = float(y)
        self.z = float(z)
    
    def __add__(self, other: 'Vector3') -> 'Vector3':
        """Vector addition with quantum precision"""
        return Vector3(self.x + other.x, self.y + other.y, self.z + other.z)
    
    def __sub__(self, other: 'Vector3') -> 'Vector3':
        """Vector subtraction with quantum precision"""
        return Vector3(self.x - other.x, self.y - other.y, self.z - other.z)
    
    def __mul__(self, scalar: float) -> 'Vector3':
        """Scalar multiplication with infinite precision"""
        return Vector3(self.x * scalar, self.y * scalar, self.z * scalar)
    
    def __truediv__(self, scalar: float) -> 'Vector3':
        """Scalar division with infinite precision"""
        if scalar == 0:
            raise ValueError("Division by zero in Vector3")
        return Vector3(self.x / scalar, self.y / scalar, self.z / scalar)
    
    def __eq__(self, other: 'Vector3') -> bool:
        """Equality comparison with quantum precision"""
        epsilon = 1e-15  # Quantum precision
        return (abs(self.x - other.x) < epsilon and 
                abs(self.y - other.y) < epsilon and 
                abs(self.z - other.z) < epsilon)
    
    def __repr__(self) -> str:
        """String representation with infinite precision"""
        return f"Vector3({self.x}, {self.y}, {self.z})"
    
    def __str__(self) -> str:
        """Human-readable string representation"""
        return f"({self.x:.6f}, {self.y:.6f}, {self.z:.6f})"
    
    def __hash__(self) -> int:
        """Hash for use in sets and dictionaries"""
        return hash((round(self.x, 10), round(self.y, 10), round(self.z, 10)))
    
    def magnitude(self) -> float:
        """Calculate vector magnitude with infinite precision"""
        return math.sqrt(self.x * self.x + self.y * self.y + self.z * self.z)
    
    def magnitude_squared(self) -> float:
        """Calculate squared magnitude for optimization"""
        return self.x * self.x + self.y * self.y + self.z * self.z
    
    def normalize(self) -> 'Vector3':
        """Normalize vector to unit length"""
        mag = self.magnitude()
        if mag == 0:
            return Vector3(0, 0, 0)
        return self / mag
    
    def distance_to(self, other: 'Vector3') -> float:
        """Calculate distance to another vector"""
        return (self - other).magnitude()
    
    def distance_squared_to(self, other: 'Vector3') -> float:
        """Calculate squared distance for optimization"""
        return (self - other).magnitude_squared()
    
    def dot(self, other: 'Vector3') -> float:
        """Dot product with quantum precision"""
        return self.x * other.x + self.y * other.y + self.z * other.z
    
    def cross(self, other: 'Vector3') -> 'Vector3':
        """Cross product with quantum precision"""
        return Vector3(
            self.y * other.z - self.z * other.y,
            self.z * other.x - self.x * other.z,
            self.x * other.y - self.y * other.x
        )
    
    def angle_to(self, other: 'Vector3') -> float:
        """Calculate angle between vectors in radians"""
        dot_product = self.dot(other)
        magnitudes = self.magnitude() * other.magnitude()
        if magnitudes == 0:
            return 0.0
        cos_angle = dot_product / magnitudes
        # Clamp to avoid numerical errors
        cos_angle = max(-1.0, min(1.0, cos_angle))
        return math.acos(cos_angle)
    
    def lerp(self, other: 'Vector3', t: float) -> 'Vector3':
        """Linear interpolation between vectors"""
        return self + (other - self) * t
    
    def slerp(self, other: 'Vector3', t: float) -> 'Vector3':
        """Spherical linear interpolation"""
        angle = self.angle_to(other)
        if angle == 0:
            return self
        
        sin_angle = math.sin(angle)
        a = math.sin((1 - t) * angle) / sin_angle
        b = math.sin(t * angle) / sin_angle
        
        return self * a + other * b
    
    def reflect(self, normal: 'Vector3') -> 'Vector3':
        """Reflect vector across a normal"""
        return self - normal * (2 * self.dot(normal))
    
    def project(self, onto: 'Vector3') -> 'Vector3':
        """Project vector onto another vector"""
        if onto.magnitude_squared() == 0:
            return Vector3(0, 0, 0)
        return onto * (self.dot(onto) / onto.magnitude_squared())
    
    def reject(self, from_vector: 'Vector3') -> 'Vector3':
        """Reject vector from another vector"""
        return self - self.project(from_vector)
    
    def rotate_around_axis(self, axis: 'Vector3', angle: float) -> 'Vector3':
        """Rotate vector around an axis using Rodrigues' rotation formula"""
        axis = axis.normalize()
        cos_angle = math.cos(angle)
        sin_angle = math.sin(angle)
        
        return (self * cos_angle + 
                axis.cross(self) * sin_angle + 
                axis * (axis.dot(self) * (1 - cos_angle)))
    
    def to_tuple(self) -> tuple:
        """Convert to tuple for serialization"""
        return (self.x, self.y, self.z)
    
    def to_dict(self) -> dict:
        """Convert to dictionary for serialization"""
        return {'x': self.x, 'y': self.y, 'z': self.z}
    
